
/**
 * 解析价格文本 (支持 $ 符号、逗号、以及下标数字表示的零)
 * 例如: "$0.0₄245" -> 0.0000245
 * @param {string} txt - 原始价格文本
 * @returns {number|null} 解析后的数值，如果无法解析则返回 null
 */
export function parsePriceText(txt) {
    if (!txt) return null;
    let s = String(txt).trim().replace(/[,\s]/g, '');
    if (s[0] === '$') s = s.slice(1);

    // [Fix] Handle GMGN format: 0.0₄245 means 0.0000245 (4 zeros)
    // But literal string is "0.0₄245".
    // "0." + "0" + "0000" = 0.00000 (5 zeros).
    // So we remove the '0' immediately preceding a subscript.
    s = s.replace(/0([₀-₉])/g, '$1');

    const map = {'₀':0,'₁':1,'₂':2,'₃':3,'₄':4,'₅':5,'₆':6,'₇':7,'₈':8,'₉':9};
    let out = '';
    
    // 预处理：检查是否包含下标数字
    // 如果包含下标，通常格式为 0.0₄245
    // 下标表示的是连续零的个数
    // 我们需要将其展开为对应数量的 '0'
    
    for (let i = 0; i < s.length; i++) {
        const c = s[i];
        if ((c >= '0' && c <= '9') || c === '.') {
            out += c;
        } else if (map.hasOwnProperty(c)) {
            // 这是一个下标数字
            // 关键修正：下标通常表示连续的零
            // 例如 0.0₄245，前面的 0.0 已经包含了一个零（小数点后的）？
            // 不，通常 0.0₄245 意思是 0.0000245 (4个零)
            // 而如果我们直接拼接，0.0 + 0000 + 245 = 0.00000245 (5个零)
            // 需要确认业界标准。
            // Birdeye/GMGN 等平台显示 0.0₄245 通常指 0.0000245 (小数点后4个零)
            // 但如果前面的文本是 0.0，那么其实已经有一个零了。
            // 让我们观察输入字符串：$0.0₄245
            // 如果我们保留前面的 0.0，再加 4个零，就是 0.00000245 (5个零)
            // 如果目的是 4个零，那么下标应该解释为 "这里有4个零"
            // 如果前文是 0.0，那么总共就是 5个零？
            // 让我们假设下标就是表示 "这里插入 N 个零"
            // 那么 0.0₄245 -> 0.0 0000 245 -> 0.00000245
            
            // 但是，用户反馈的问题可能是 "价格不显示"
            // 如果解析出 0.00000245，它是 > 0 的，应该显示。
            // 除非... 解析结果为 NaN 或 null。
            // 原来的逻辑是：
            // out += '0'.repeat(map[c]);
            // 这逻辑本身没问题，只要 map[c] 是对的。
            
            out += '0'.repeat(map[c]);
        }
    }
    
    const n = parseFloat(out);
    return isNaN(n) ? null : n;
}
